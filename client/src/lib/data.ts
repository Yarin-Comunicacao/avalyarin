export interface MenuItem {
  id: string;
  name: string;
  description: string;
  price: number;
  category: "entrada" | "prato" | "sobremesa" | "bebida" | "chopp" | "drink" | "destilado";
}

export interface Establishment {
  id: string;
  name: string;
  address: string;
  neighborhood: string;
  rating: number;
  reviewCount: number;
  image: string;
  hours: string;
  phone: string;
  instagram?: string;
  menu: MenuItem[];
}

export interface Category {
  id: string;
  name: string;
  description: string;
  icon: string;
  active: boolean;
  establishments: Establishment[];
}

export interface RatingCriterion {
  id: string;
  name: string;
  weight: number;
  description: string;
  subcriteria: { id: string; name: string; description: string }[];
}

